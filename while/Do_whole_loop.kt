/*Task
Write a program which does the following

Declare an integer variable i and initialise it to 0
Use the do..while loop to output i to the console as long as i is less than 6.*/
//Solution as follows

fun main(args : Array<String>) {
    var i = 0
    do {
        println(i)  
        i++
    }
    while (i < 6) 
}